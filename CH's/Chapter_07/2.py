l = ["Harry", "Aditya", "Ayush", "Rahul","Ankita"]
for name in l:
    if(name.startswith("A")):
        print(f"Hello Good Morning {name}")
#this code iterates through a list of names and prints a greeting for each name that starts with the letter "A".
# For example, if the list contains "Harry", "Aditya", "Ayush", "Rahul", and "Ankita",
# it will print:
# Hello Good Morning Aditya
# Hello Good Morning Ayush
# Hello Good Morning Ankita